                <h2>BIGH Urban Farm: Agriculture Reimagined</h2>
                <p class="lead">The World's Largest Rooftop Farm</p>
                
                <h5 class="text-gold mt-4">Our Visit - Day 6 (October 16, 2025)</h5>
                <p>We visited BIGH (Building Integrated Greenhouses) in Anderlecht, an revolutionary urban agriculture project that demonstrates how cities can produce food sustainably.</p>
                
                <h6 class="text-gold mt-3">Key Features:</h6>
                <ul>
                    <li><strong>Size:</strong> 4,000 m² rooftop greenhouse</li>
                    <li><strong>Production:</strong> 50 tons of vegetables and 35,000 fish annually</li>
                    <li><strong>Technology:</strong> Advanced aquaponics systems</li>
                    <li><strong>Location:</strong> Built on top of a former industrial site</li>
                    <li><strong>Impact:</strong> Provides fresh, local food to Brussels residents</li>
                </ul>
                
                <h6 class="text-gold mt-3">Innovations:</h6>
                <ul>
                    <li>Aquaponics: Fish waste fertilizes plants, plants filter water for fish</li>
                    <li>Heat recovery from adjacent waste treatment facility</li>
                    <li>Rainwater collection and efficient water use</li>
                    <li>Vertical farming maximizing limited urban space</li>
                    <li>Zero pesticides, 100% sustainable practices</li>
                </ul>
                
                <h6 class="text-gold mt-3">What We Learned:</h6>
                <p>BIGH Farm demonstrated that cities don't need vast rural areas to produce food. By utilizing unused rooftop space and implementing circular economy principles, urban areas can become more self-sufficient and reduce their carbon footprint from food transportation.</p>
                
                <p class="text-muted small"><em>"Seeing fish and vegetables growing together in the heart of the city was eye-opening. This model could work in Tanzanian cities too!"</em></p>
            </div>
            <div class="col-lg-6 order-lg-1" data-aos="fade-right">
                <div class="card-custom mb-3">
                    <div class="card-body">
                        <h5 class="text-gold">Circular Economy Principles</h5>
                        <p>BIGH Farm exemplifies the circular economy:</p>
                        <ol>
                            <li>Fish produce nutrient-rich waste</li>
                            <li>Bacteria convert waste into plant fertilizer</li>
                            <li>Plants absorb nutrients and purify water</li>
                            <li>Clean water returns to fish tanks</li>
                            <li>Heat from adjacent facility warms greenhouses</li>
                        </ol>
