    <div class="container">
        <div class="row align-items-center">
            <div class="col-lg-6" data-aos="fade-right">
                <h2>Belgian Federal Parliament</h2>
                <p class="lead">Belgium's Complex Federal System</p>
                
                <h5 class="text-gold mt-4">Our Visit - Day 6 (October 16, 2025)</h5>
                <p>We visited the Belgian Federal Parliament to understand Belgium's unique federal structure and constitutional monarchy.</p>
                
                <h6 class="text-gold mt-3">Structure:</h6>
                <ul>
                    <li><strong>Chamber of Representatives:</strong> 150 members, directly elected</li>
                    <li><strong>Senate:</strong> 60 members, representing communities and regions</li>
                    <li><strong>Government:</strong> Led by Prime Minister</li>
                    <li><strong>Monarchy:</strong> Constitutional, King Philippe as Head of State</li>
                </ul>
                
                <h6 class="text-gold mt-3">Federal System:</h6>
                <p>Belgium is divided into:</p>
                <ul>
                    <li><strong>3 Regions:</strong> Flanders, Wallonia, Brussels-Capital</li>
                    <li><strong>3 Communities:</strong> Flemish, French, German-speaking</li>
                    <li>Each has its own parliament and government</li>
                    <li>Powers divided between federal and regional levels</li>
                </ul>
                
                <h6 class="text-gold mt-3">Linguistic Complexity:</h6>
                <p>Belgium has three official languages:</p>
                <ul>
                    <li>Dutch (Flemish) - 60% of population</li>
                    <li>French - 40% of population</li>
                    <li>German - small minority in eastern Belgium</li>
                </ul>
                <p>This diversity has shaped Belgium's political system to ensure representation for all linguistic communities.</p>
            </div>
            <div class="col-lg-6" data-aos="fade-left">
                <div class="card-custom mb-3">
                    <div class="card-body">
                        <h5 class="text-gold">How Laws Are Made in Belgium</h5>
                        <ol>
                            <li>Government or MPs propose legislation</li>
                            <li>Committees examine the proposal</li>
                            <li>Chamber of Representatives debates and votes</li>
                            <li>Senate may review (limited powers since 2014 reform)</li>
                            <li>King signs the law (formal approval)</li>
                            <li>Law is published and enters into force</li>
                        </ol>
                    </div>
                </div>
                <div class="card-custom">
                    <div class="card-body">
                        <h5 class="text-gold">Coalition Government</h5>
                        <p>Belgium typically has coalition governments because:</p>
                        <ul class="small">
                            <li>Multiple political parties share power</li>
                            <li>Requires compromise and negotiation</li>
                            <li>Ensures representation of different regions and communities</li>
                            <li>Sometimes takes months to form after elections</li>
                        </ul>
                        <p class="small mb-0">This system promotes consensus-building but can be slow in decision-making.</p>
                    </div>
                </div>
            </div>
        </div>
    </div>
</section>

